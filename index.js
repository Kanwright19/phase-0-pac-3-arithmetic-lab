function add(a,b) {
 let sum = a + b; {
    return sum;
    }
}

function subtract(a,b) {
 let difference = a - b; {
    return difference;
    }
}

function multiply (a,b) {
 let product = a * b; {
    return product;
    }
}

function divide(a, b) {
 let quotient = a / b; {
    return quotient;
    }
}


function increment(n) {
    return n + 1;
   }


function decrement(n) {
    return n - 1;
}


function makeInt(n) {
    let base = 10
 return parseInt(n,base);
}


function preserveDecimal(n) {
    let base = 10
 return parseFloat(n,base)
}